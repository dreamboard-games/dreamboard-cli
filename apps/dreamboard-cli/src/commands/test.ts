function tryJsonParse(value: unknown): unknown {
  return typeof value === "string" ? JSON.parse(value) : value;
}

const DEFAULT_TIMEOUT_MS = 10_000;
const TEST_SCENARIO_SUFFIX = ".scenario.ts";
const TESTING_TYPES_STUB =
  "export function defineScenario(scenario) { return scenario; }\n";
const GENERATED_TESTING_TYPES_PREFIX = "// Generated by dreamboard";

type ScenarioDefinition = {
  meta: {
    id: string;
    description: string;
  };
  given: {
    base: string;
  };
  when: {
    steps: ApiScenarioStep[];
  };
  then: {
    assert: (ctx: {
      gameState: Record<string, unknown>;
      events: EventInspector;
      expect: ExpectApi;
    }) => void | Promise<void>;
  };
};

type LoadedScenario = {
  filePath: string;
  scenario: ScenarioDefinition;
};

type BaseScenarioDefinition = {
  seed: number;
  players: number;
  steps: ApiScenarioStep[];
};

type BaseStateArtifact = {
  key: string;
  base: string;
  setupSteps: ApiScenarioStep[];
  snapshot: Record<string, unknown>;
  fingerprint: {
    base: string;
    seed: number;
    players: number;
    baseSetupHash: string;
    compiledResultId: string;
    manifestHash?: string;
    gameId: string;
  };
};

type BaseStateFile = Record<string, BaseStateArtifact>;

type BaseScenarioDefinitions = Record<string, BaseScenarioDefinition>;

type EventRecord = {
  type: GameMessage["type"];
  message: GameMessage;
};

type TestCommandRuntime = {
  gameId: string;
  compiledResultId: string;
  manifestHash?: string;
  cleanup: () => Promise<void>;
};

type ExpectMatchers = {
  toBe: (expected: unknown) => void;
  toEqual: (expected: unknown) => void;
  toBeDefined: () => void;
  toBeUndefined: () => void;
  toContain: (expected: unknown) => void;
  toBeGreaterThanOrEqual: (expected: number) => void;
};

type ExpectApi = (actual: unknown) => ExpectMatchers;

const apiScenarioStepSchema = z
  .object({
    playerId: z.string().min(1),
    actionType: z.string().min(1),
    parameters: z.record(z.string(), z.unknown()).optional(),
    turns: z.number().int().positive().optional(),
  })
  .strict();

const scenarioSchema = z
  .object({
    meta: z
      .object({
        id: z.string().min(1),
        description: z.string().min(1),
      })
      .strict(),
    given: z
      .object({
        base: z.string().min(1),
      })
      .strict(),
    when: z
      .object({
        steps: z.array(apiScenarioStepSchema),
      })
      .strict(),
    then: z
      .object({
        assert: z.custom<(ctx: unknown) => void | Promise<void>>(
          (value) => typeof value === "function",
        ),
      })
      .strict(),
  })
  .strict();

function formatValidationIssues(error: z.ZodError): string {
  return error.issues
    .map((issue) => `${issue.path.join(".") || "scenario"}: ${issue.message}`)
    .join("; ");
}

export function parseScenarioDefinition(value: unknown): ScenarioDefinition {
  const parsed = scenarioSchema.safeParse(value);
  if (!parsed.success) {
    throw new Error(formatValidationIssues(parsed.error));
  }
  return parsed.data;
}

const generateCommand = defineCommand({
  meta: {
    name: "generate",
    description: "Generate deterministic base snapshots for TS scenarios",
  },
  args: {
    scenario: {
      type: "string",
      description: "Optional scenario file path under test/scenarios",
    },
    ...CONFIG_FLAG_ARGS,
  },
  async run({ args }) {
    const parsedFlags = parseConfigFlags(args);
    const useRemoteRuntime = shouldUseRemoteTestRuntime(parsedFlags.env);
    const { projectRoot, projectConfig } = await resolveProjectContext(
      parsedFlags,
      { requireAuth: useRemoteRuntime },
    );
    await ensureScenarioImportShim(projectRoot);

    const scenarios = await loadScenarios(projectRoot, {
      scenarioPath: args.scenario,
    });
    const baseScenarios = await loadBaseScenarioDefinitions(projectRoot);

    if (scenarios.length === 0) {
      throw new Error("No scenarios found under test/scenarios/*.scenario.ts");
    }

    validateScenarioBases(scenarios, baseScenarios);

    const runtime = await resolveTestCommandRuntime({
      projectRoot,
      projectConfig,
      useRemoteRuntime,
    });

    try {
      const uniqueBases = new Set<string>();
      for (const scenario of scenarios) {
        uniqueBases.add(scenario.scenario.given.base);
      }

      const generatedBaseStates: BaseStateFile = {};
      for (const base of uniqueBases.values()) {
        const baseScenario = baseScenarios[base];
        if (!baseScenario) continue;
        const snapshot = await captureBaseSnapshot({
          gameId: runtime.gameId,
          compiledResultId: runtime.compiledResultId,
          baseSteps: baseScenario.steps,
          seed: baseScenario.seed,
          players: baseScenario.players,
        });
        const key = baseStateKey(base);
        generatedBaseStates[key] = {
          key,
          base,
          setupSteps: baseScenario.steps,
          snapshot,
          fingerprint: {
            base,
            seed: baseScenario.seed,
            players: baseScenario.players,
            baseSetupHash: computeBaseSetupHash(baseScenario.steps),
            compiledResultId: runtime.compiledResultId,
            manifestHash: runtime.manifestHash,
            gameId: runtime.gameId,
          },
        };
      }

      const generatedDir = path.join(projectRoot, "test", "generated");
      await ensureDir(generatedDir);
      await writeGeneratedFiles({
        generatedDir,
        baseStates: generatedBaseStates,
        scenarios,
      });

      consola.success(
        `Generated ${Object.keys(generatedBaseStates).length} base state(s) for ${scenarios.length} scenario(s).`,
      );
    } finally {
      await runtime.cleanup();
    }
  },
});

const runCommand = defineCommand({
  meta: {
    name: "run",
    description:
      "Run TypeScript scenarios from test/scenarios using generated bases",
  },
  args: {
    scenario: {
      type: "string",
      description: "Optional scenario file path under test/scenarios",
    },
    ...CONFIG_FLAG_ARGS,
  },
  async run({ args }) {
    const parsedFlags = parseConfigFlags(args);
    const useRemoteRuntime = shouldUseRemoteTestRuntime(parsedFlags.env);
    const { projectRoot, projectConfig } = await resolveProjectContext(
      parsedFlags,
      { requireAuth: useRemoteRuntime },
    );
    await ensureScenarioImportShim(projectRoot);

    const scenarios = await loadScenarios(projectRoot, {
      scenarioPath: args.scenario,
    });
    const baseScenarios = await loadBaseScenarioDefinitions(projectRoot);

    if (scenarios.length === 0) {
      throw new Error("No scenarios found under test/scenarios/*.scenario.ts");
    }

    validateScenarioBases(scenarios, baseScenarios);

    const baseStates = await loadGeneratedBaseStates(projectRoot);
    const runtime = await resolveTestCommandRuntime({
      projectRoot,
      projectConfig,
      useRemoteRuntime,
    });

    try {
      let passed = 0;
      let failed = 0;
      for (const scenario of scenarios) {
        try {
          await runSingleScenario({
            scenario,
            baseScenario: baseScenarios[scenario.scenario.given.base]!,
            baseStates,
            runtimeFingerprint: {
              compiledResultId: runtime.compiledResultId,
              manifestHash: runtime.manifestHash,
              gameId: runtime.gameId,
            },
            gameId: runtime.gameId,
            compiledResultId: runtime.compiledResultId,
          });
          passed += 1;
          consola.success(`PASS ${scenario.scenario.meta.id}`);
        } catch (error) {
          failed += 1;
          consola.error(
            `FAIL ${scenario.scenario.meta.id}: ${formatScenarioError(error)}`,
          );
        }
      }
      consola.info(`Test summary: ${passed} passed, ${failed} failed.`);
      if (failed > 0) {
        process.exitCode = 1;
      }
    } finally {
      await runtime.cleanup();
    }
  },
});

async function resolveTestCommandRuntime(options: {
  projectRoot: string;
  projectConfig: ProjectConfig;
  useRemoteRuntime: boolean;
}): Promise<TestCommandRuntime> {
  if (options.useRemoteRuntime) {
    const latestCompiledResult = await resolveCompiledResultForRun(
      options.projectRoot,
      options.projectConfig,
    );
    return {
      gameId: options.projectConfig.gameId,
      compiledResultId: latestCompiledResult.id,
      manifestHash: options.projectConfig.manifestContentHash,
      cleanup: async () => undefined,
    };
  }

  const harness = await startLocalHarnessSession({
    projectRoot: options.projectRoot,
    projectConfig: options.projectConfig,
  });

  return {
    gameId: harness.gameId,
    compiledResultId: harness.compiledResultId,
    manifestHash: harness.manifestHash,
    cleanup: harness.stop,
  };
}

