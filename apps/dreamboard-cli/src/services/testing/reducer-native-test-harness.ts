function resolveEffectiveBaseSetup(options: {
  manifest: GameTopologyManifest;
  base: TypedBaseDefinition;
  basesById?: Map<string, LoadedBase>;
}): EffectiveBaseSetup {
  const inheritedSetupProfileId =
    options.base.setupProfileId ??
    (options.base.extends && options.basesById
      ? resolveEffectiveBaseSetup({
          manifest: options.manifest,
          base:
            options.basesById.get(options.base.extends)?.definition ??
            (() => {
              throw new Error(
                `Base '${options.base.id}' extends unknown parent '${options.base.extends}'.`,
              );
            })(),
          basesById: options.basesById,
        }).setupProfileId
      : undefined);
  const selection = resolveSetupProfileSelection({
    manifest: options.manifest,
    requestedSetupProfileId: inheritedSetupProfileId ?? undefined,
  });
  return {
    setupProfileId: selection.id,
  };
}

function resolveBaseDefinition(
  base: LoadedBase,
  basesById: Map<string, LoadedBase>,
): ResolvedBaseDefinition {
  const inherited =
    base.definition.extends !== undefined
      ? resolveBaseDefinition(
          basesById.get(base.definition.extends) ??
            (() => {
              throw new Error(
                `Base '${base.definition.id}' extends unknown parent '${base.definition.extends}'.`,
              );
            })(),
          basesById,
        )
      : null;

  const seed = base.definition.seed ?? inherited?.seed;
  const players = base.definition.players ?? inherited?.players;
  if (seed === undefined || players === undefined) {
    throw new Error(
      `Base '${base.definition.id}' must resolve numeric seed and players.`,
    );
  }

  return {
    ...base.definition,
    seed,
    players,
    setupProfileId:
      base.definition.setupProfileId ?? inherited?.setupProfileId ?? undefined,
  };
}

function summarizeTableValidationError(
  manifest: GameTopologyManifest,
  error: Error,
): string {
  const issues = error.message
    .split("; ")
    .map((issue) => issue.trim())
    .filter((issue) => issue.length > 0);

  if (
    issues.length === 0 ||
    !issues.every((issue) => issue.startsWith("table"))
  ) {
    return error.message;
  }

  const boardBaseIdByRuntimeId = new Map<string, string>(
    (manifest.boards ?? []).flatMap((board) =>
      board.scope === "perPlayer"
        ? Array.from(
            { length: manifest.players.maxPlayers },
            (_, index) =>
              [`${board.id}:player-${index + 1}`, board.id] as const,
          )
        : ([[board.id, board.id]] as const),
    ),
  );

  const representativeIssues = issues.slice(0, 3).map((issue) => {
    const [, runtimeBoardId = ""] =
      issue.match(/^table\.boards\.byId\.([^.]+)\./) ?? [];
    const boardBaseId = boardBaseIdByRuntimeId.get(runtimeBoardId);
    if (!boardBaseId) {
      return `- ${issue.replace(/^table\./, "")}`;
    }
    const boardSpecificPath = issue
      .replace(/^table\.boards\.byId\.[^.]+\./, "")
      .replace(/^table\./, "");
    return `- board '${boardBaseId}' (${runtimeBoardId}): ${boardSpecificPath}`;
  });

  return [
    `Reducer-native table validation failed with ${issues.length} issue${issues.length === 1 ? "" : "s"}.`,
    "Likely root cause: the generated test table shape does not match the authored manifest topology.",
    ...representativeIssues,
    "Pass --debug for the full validation dump.",
  ].join("\n");
}

class ShadowReducerRuntime {
  private readonly bundle: ReturnType<typeof createReducerBundle>;
  private readonly playerIds: string[];
  private readonly historyRecords: HistoryRecord[] = [];
  private state: Record<string, unknown> | null = null;
  private started = false;

  constructor(
    private readonly manifest: GameTopologyManifest,
    private readonly gameModuleDefault: unknown,
    private readonly createInitialTable: GeneratedInitialTableFactory | null,
    private readonly seed: number,
    private readonly players: number,
    private readonly setupProfileId: string | null,
    private readonly debug = false,
  ) {
    if (
      typeof gameModuleDefault !== "object" ||
      gameModuleDefault === null ||
      !("contract" in gameModuleDefault)
    ) {
      throw new Error(
        "app/game.ts must export a reducer-native game definition.",
      );
    }
    this.bundle = createReducerBundle(gameModuleDefault as never);
    this.playerIds = Array.from(
      { length: players },
      (_, index) => `player-${index + 1}`,
    );
  }

  async start(): Promise<void> {
    if (this.started) {
      return;
    }
    const random = new KotlinSeededRandom(this.seed);
    const shuffleItems = <Value>(values: readonly Value[]) =>
      shuffleWithKotlinRandom(values, random);
    const table =
      this.createInitialTable?.({
        playerIds: this.playerIds,
        shuffleItems,
      }) ??
      materializeManifestTable({
        manifest: this.manifest,
        playerIds: this.playerIds,
        shuffleItems,
      });
    const rngSeed = random.nextLong();
    try {
      this.state = (await this.bundle.initialize({
        table: table as never,
        playerIds: this.playerIds,
        rngSeed,
        setup: this.setupProfileId
          ? {
              profileId: this.setupProfileId,
            }
          : null,
      })) as Record<string, unknown>;
    } catch (error) {
      if (!this.debug && error instanceof Error) {
        throw new Error(summarizeTableValidationError(this.manifest, error));
      }
      throw error;
    }
    this.started = true;
  }

  phase(): string {
    return (
      ((
        (this.state?.domain as { flow?: { currentPhase?: string } } | undefined)
          ?.flow as { currentPhase?: string } | undefined
      )?.currentPhase ??
        null) ||
      ""
    );
  }

  playerOrder(): string[] {
    return this.playerIds;
  }

  rawState(): Record<string, unknown> {
    if (!this.state) {
      throw new Error("Reducer runtime has not been started.");
    }
    return this.state;
  }

  hydrate(snapshot: Record<string, unknown>): void {
    this.state = structuredClone(snapshot);
    this.started = true;
  }

  projectAllSeats(): {
    currentStage: string | null;
    stageSeats: string[];
    seats: Record<
      string,
      {
        view?: unknown;
        availableInteractions?: Array<Record<string, unknown>>;
        zones?: Record<string, unknown>;
      }
    >;
  } {
    const projection = this.bundle.projectSeatsDynamic({
      state: this.rawState() as never,
      playerIds: this.playerIds as never,
    }) as {
      currentStage?: string | null;
      stageSeats?: string[];
      seats?: Record<
        string,
        {
          view?: unknown;
          availableInteractions?: Array<Record<string, unknown>>;
          zones?: Record<string, unknown>;
        }
      >;
    };
    return {
      currentStage: projection.currentStage ?? null,
      stageSeats: projection.stageSeats ?? [],
      seats: projection.seats ?? {},
    };
  }

  currentStage(): string | null {
    return this.projectAllSeats().currentStage;
  }

  view(playerId: string): unknown {
    return this.projectAllSeats().seats[playerId]?.view ?? null;
  }

  availableInteractionsForPlayer(playerId: string): string[] {
    const projection = this.projectAllSeats();
    const seat = projection.seats[playerId];
    const descriptors = seat?.availableInteractions ?? [];
    return descriptors
      .filter(
        (descriptor) =>
          !!descriptor && typeof descriptor.interactionId === "string",
      )
      .map((descriptor) => descriptor.interactionId as string);
  }

  /**
   * Phase-kind interaction descriptors (action + prompt) available to
   * `playerId` in the current phase. Surfaces the same projection the UI
   * SDK consumes, so scenarios can assert addressee authorization and
   * availability without reaching into game-specific state. Returned as
   * loose records — the typed `InteractionDescriptor` shape is narrowed
   * downstream in the workspace-generated `testing-contract.ts`.
   */
  interactionsForPlayer(playerId: string): Array<Record<string, unknown>> {
    return this.projectAllSeats().seats[playerId]?.availableInteractions ?? [];
  }

  clearHistory(): void {
    this.historyRecords.length = 0;
  }

  private async applyInput(input: HistoryInput): Promise<void> {
    const parsedInput = toReducerInput(input);
    const validation = parseWirePayload(
      "validateInput",
      await this.bundle.validateInput({
        state: this.rawState() as never,
        input: parsedInput as never,
      }),
      "ReducerInputValidationResult",
      ReducerContractZod.ReducerInputValidationResultSchema,
    );
    if (!validation.valid) {
      const record = {
        input,
        accepted: false,
        errorCode: validation.errorCode,
        message: validation.message,
      };
      this.historyRecords.push(record);
      throw createSubmissionError(
        validation.errorCode,
        validation.message,
        "Reducer input rejected.",
      );
    }

    const result = assertDispatchResultWireContract(
      await this.bundle.dispatch({
        state: this.rawState() as never,
        input: parsedInput as never,
      }),
    );

    if (result.kind === "reject") {
      const record = {
        input,
        accepted: false,
        errorCode: result.errorCode,
        message: result.message,
      };
      this.historyRecords.push(record);
      throw createSubmissionError(
        result.errorCode,
        result.message,
        "Reducer input rejected.",
      );
    }

    this.state = result.state as Record<string, unknown>;
    this.historyRecords.push({ input, accepted: true });
  }

  async submitInteraction(
    playerId: string,
    interactionId: string,
    params: Record<string, unknown>,
  ): Promise<void> {
    await this.applyInput({
      kind: "interaction",
      playerId,
      interactionId,
      params,
    });
  }
}

/**
 * Translate the harness's history record into the unified `interaction`
 * wire shape the ingress bundle accepts.
 */
function toReducerInput(input: HistoryInput): Record<string, unknown> {
  return {
    kind: "interaction",
    playerId: input.playerId,
    interactionId: input.interactionId,
    params: input.params,
  };
}

class EmbeddedGameplayTracker {
  private readonly abortController = new AbortController();
  private readonly state: GameplaySnapshot = {
    version: -1,
    actionSetVersion: "",
    currentPhase: null,
    playerId: "player-1",
    view: null,
    availableInteractions: [],
  };
  private readonly events: GameMessage[] = [];
  private connected = false;

  constructor(
    private readonly sessionId: string,
    private readonly playerId: string,
  ) {
    this.state.playerId = playerId;
  }

  async connect(): Promise<void> {
    if (this.connected) {
      return;
    }
    this.connected = true;
    const { stream } = await subscribeToCliSessionEvents({
      sessionId: this.sessionId,
      playerId: this.playerId,
      signal: this.abortController.signal,
      clientSource: "dreamboard-cli-test",
    });

    void (async () => {
      for await (const message of stream) {
        if (!message) {
          continue;
        }
        this.events.push(message);
        this.onMessage(message);
      }
    })();
  }

  close(): void {
    this.abortController.abort();
  }

  snapshot(): GameplaySnapshot {
    return {
      ...this.state,
      availableInteractions: [...this.state.availableInteractions],
    };
  }

  private onMessage(message: GameMessage): void {
    if (message.type === "gameplay.bootstrap") {
      this.state.version = message.gameplay.version;
      this.state.actionSetVersion = message.gameplay.actionSetVersion;
      this.state.playerId = message.gameplay.playerId;
      this.state.currentPhase = message.gameplay.currentPhase;
      this.state.view = parseSeatView(message.gameplay.view);
      this.state.availableInteractions = (
        message.gameplay.availableInteractions ?? []
      ).map((interaction) => interaction.interactionId);
      return;
    }

    if (
      message.type === "gameplay.updated" ||
      message.type === "gameplay.resynced"
    ) {
      this.state.version = message.gameplay.version;
      this.state.actionSetVersion = message.gameplay.actionSetVersion;
      this.state.playerId = message.gameplay.playerId;
      this.state.currentPhase = message.gameplay.currentPhase;
      this.state.view = parseSeatView(message.gameplay.view);
      this.state.availableInteractions = (
        message.gameplay.availableInteractions ?? []
      ).map((interaction) => interaction.interactionId);
    }
  }

  async waitForVersionChange(
    previousVersion: number,
    timeoutMs = DEFAULT_TIMEOUT_MS,
  ): Promise<void> {
    const startedAt = Date.now();
    while (Date.now() - startedAt < timeoutMs) {
      if (this.state.version > previousVersion) {
        return;
      }
      await sleep(20);
    }
    throw new Error("Timed out waiting for gameplay version change.");
  }

  async waitForBootstrap(timeoutMs = DEFAULT_TIMEOUT_MS): Promise<void> {
    await this.waitForVersionChange(-1, timeoutMs);
  }
}

function parseSeatView(raw: string | null | undefined): unknown {
  return raw ? parseJsonValue(raw) : null;
}

function sleep(ms: number): Promise<void> {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function formatIssuePath(
  label: string,
  path: ReadonlyArray<PropertyKey>,
): string {
  let formatted = label;
  for (const segment of path) {
    formatted +=
      typeof segment === "number" ? `[${segment}]` : `.${String(segment)}`;
  }
  return formatted;
}

function parseWirePayload<T>(
  methodName: string,
  value: unknown,
  label: string,
  schema: {
    safeParse: (input: unknown) =>
      | { success: true; data: T }
      | {
          success: false;
          error: {
            issues: Array<{
              path: Array<PropertyKey>;
              message: string;
            }>;
          };
        };
  },
): T {
  const parsed = schema.safeParse(value);
  if (parsed.success) {
    return parsed.data;
  }
  const firstIssue = parsed.error.issues[0];
  const formattedPath = formatIssuePath(label, firstIssue?.path ?? []);
  throw new Error(
    `Reducer bundle returned invalid payload for '${methodName}': ${formattedPath} ${firstIssue?.message ?? "failed schema validation"}.`,
  );
}

export function assertDispatchResultWireContract(
  result: unknown,
): Wire.DispatchResult {
  return parseWirePayload(
    "dispatch",
    result,
    "DispatchResult",
    ReducerContractZod.DispatchResultSchema,
  );
}

async function createBrowserBridgeClient(
  page: Page,
): Promise<BrowserBridgeClient> {
  const bridgeExists = async (): Promise<boolean> =>
    page.evaluate(() =>
      Boolean(
        (
          window as typeof window & {
            __dreamboardTestBridge__?: unknown;
          }
        ).__dreamboardTestBridge__,
      ),
    );

  const startedAt = Date.now();
  while (!(await bridgeExists())) {
    if (Date.now() - startedAt > DEFAULT_TIMEOUT_MS) {
      const bodyText = await page
        .locator("body")
        .innerText()
        .catch(() => "");
      const diagnostic = bodyText.trim().replace(/\s+/g, " ").slice(0, 400);
      throw new Error(
        diagnostic.length > 0
          ? `Timed out waiting for browser test bridge. Page text: ${diagnostic}`
          : "Timed out waiting for browser test bridge.",
      );
    }
    await sleep(50);
  }

  return {
    snapshot: () =>
      page.evaluate(() =>
        (
          window as typeof window & {
            __dreamboardTestBridge__: {
              snapshot: () => Promise<BrowserSnapshot>;
            };
          }
        ).__dreamboardTestBridge__.snapshot(),
      ),
    submitInteraction: (playerId, interactionId, params) =>
      page.evaluate(
        ([nextPlayerId, nextInteractionId, nextParams]) =>
          (
            window as typeof window & {
              __dreamboardTestBridge__: {
                submitInteraction: (
                  playerId: string,
                  interactionId: string,
                  params: unknown,
                ) => Promise<void>;
              };
            }
          ).__dreamboardTestBridge__.submitInteraction(
            nextPlayerId,
            nextInteractionId,
            nextParams,
          ),
        [playerId, interactionId, params] as const,
      ),
    waitForVersionChange: async (
      previousVersion,
      timeoutMs = DEFAULT_TIMEOUT_MS,
    ) => {
      const started = Date.now();
      while (Date.now() - started < timeoutMs) {
        const snapshot = await page.evaluate(() =>
          (
            window as typeof window & {
              __dreamboardTestBridge__: {
                snapshot: () => Promise<BrowserSnapshot>;
              };
            }
          ).__dreamboardTestBridge__.snapshot(),
        );
        if (snapshot.version > previousVersion) {
          return snapshot;
        }
        await sleep(50);
      }
      throw new Error("Timed out waiting for browser gameplay version change.");
    },
  };
}

async function assertLiveMatchesShadow(
  runner: TestRunnerName,
  shadow: ShadowReducerRuntime,
  live: GameplaySnapshot | BrowserSnapshot,
): Promise<void> {
  if (runner === "reducer") {
    return;
  }

  const currentPhase =
    "currentPhase" in live ? live.currentPhase : shadow.phase();
  if (currentPhase !== shadow.phase()) {
    throw new Error(
      `Live phase '${String(currentPhase)}' diverged from reducer phase '${shadow.phase()}'.`,
    );
  }

  const livePlayerId =
    "playerId" in live ? live.playerId : live.controllingPlayerId;
  const liveView = "view" in live ? live.view : null;
  if (livePlayerId && !deepEqual(liveView, await shadow.view(livePlayerId))) {
    throw new Error("Live projected views diverged from reducer shadow views.");
  }

  const liveInteractions =
    "availableInteractions" in live ? live.availableInteractions : null;
  if (
    livePlayerId &&
    liveInteractions &&
    !deepEqual(
      liveInteractions,
      shadow.availableInteractionsForPlayer(livePlayerId),
    )
  ) {
    throw new Error(
      "Live available-interactions metadata diverged from reducer shadow state.",
    );
  }
}

