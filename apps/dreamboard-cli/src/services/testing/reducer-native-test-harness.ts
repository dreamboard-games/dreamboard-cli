async function createScenarioContext(options: {
  runner: TestRunnerName;
  shadow: ShadowReducerRuntime;
  embedded?:
    | {
        sessionId: string;
        tracker: EmbeddedGameplayTracker;
      }
    | undefined;
  browser?:
    | {
        bridge: BrowserBridgeClient;
        driver: BrowserRunnerDriver | null;
      }
    | undefined;
}): Promise<SharedScenarioContext> {
  const expect = createExpectApi();

  const resolvePromptInstanceId = (
    playerId: string,
    promptOrId: string | { id: string; promptId?: string },
  ): string => {
    if (typeof promptOrId !== "string") {
      return (
        (promptOrId.id as string | undefined) ??
        (promptOrId.promptId as string | undefined) ??
        ""
      );
    }

    const directMatch = options.shadow
      .promptInstances()
      .find((candidate) => candidate.id === promptOrId);
    if (directMatch) {
      return promptOrId;
    }

    const activePrompt = options.shadow
      .promptsForPlayer(playerId)
      .find((candidate) => candidate.promptId === promptOrId);
    return (activePrompt?.id as string | undefined) ?? promptOrId;
  };

  const resolveWindowInstanceId = (
    playerId: string,
    windowOrId: string | { id: string; windowId?: string },
  ): string => {
    if (typeof windowOrId !== "string") {
      return (
        (windowOrId.id as string | undefined) ??
        (windowOrId.windowId as string | undefined) ??
        ""
      );
    }

    const directMatch = options.shadow
      .windowInstances()
      .find((candidate) => candidate.id === windowOrId);
    if (directMatch) {
      return windowOrId;
    }

    const activeWindow = options.shadow
      .windowsForPlayer(playerId)
      .find((candidate) => candidate.windowId === windowOrId);
    return (activeWindow?.id as string | undefined) ?? windowOrId;
  };

  const api: ScenarioGameApi = {
    start: async () => {
      await options.shadow.start();
      if (options.runner === "embedded" && options.embedded) {
        const live = options.embedded.tracker.snapshot();
        await assertLiveMatchesShadow("embedded", options.shadow, live);
      }
      if (options.runner === "browser" && options.browser) {
        const live = await options.browser.bridge.snapshot();
        await assertLiveMatchesShadow("browser", options.shadow, live);
      }
    },
    action: async (playerId, actionOrCommand, params) => {
      await api.start();
      const actionType =
        typeof actionOrCommand === "string"
          ? actionOrCommand
          : actionOrCommand.type;
      const actionParams =
        typeof actionOrCommand === "string"
          ? (params ?? {})
          : (actionOrCommand.params ?? {});

      const previousVersion =
        options.runner === "embedded"
          ? (options.embedded?.tracker.snapshot().version ?? 0)
          : options.runner === "browser"
            ? ((await options.browser?.bridge.snapshot())?.version ?? 0)
            : 0;

      if (options.runner === "embedded" && options.embedded) {
        const { data, error } = await submitInput({
          path: { sessionId: options.embedded.sessionId },
          body: {
            input: {
              kind: "action",
              playerId,
              actionType,
              params: JSON.stringify(actionParams),
            },
            expectedVersion: previousVersion,
          },
        });
        if (error) {
          throw createSubmissionError(
            "api-error",
            undefined,
            "Failed to submit action",
          );
        }
        if (data?.accepted === false) {
          const shadowError = await tryShadowAction(
            options.shadow,
            playerId,
            actionType,
            actionParams,
          );
          if (!shadowError || shadowError.errorCode !== data.errorCode) {
            throw new Error(
              "Embedded harness rejection diverged from reducer shadow state.",
            );
          }
          throw createSubmissionError(
            data.errorCode ?? undefined,
            data.message ?? undefined,
            "Action rejected",
          );
        }
      } else if (options.runner === "browser" && options.browser) {
        const handled =
          (await options.browser.driver?.action?.(options.browser.bridge, {
            playerId,
            actionType,
            params: actionParams,
          })) === true;
        if (!handled) {
          await options.browser.bridge.submitAction(
            playerId,
            actionType,
            actionParams,
          );
        }
      }

      await options.shadow.submitAction(playerId, actionType, actionParams);

      if (options.runner === "embedded" && options.embedded) {
        await options.embedded.tracker.waitForVersionChange(previousVersion);
        await assertLiveMatchesShadow(
          "embedded",
          options.shadow,
          options.embedded.tracker.snapshot(),
        );
      }
      if (options.runner === "browser" && options.browser) {
        const live =
          await options.browser.bridge.waitForVersionChange(previousVersion);
        await assertLiveMatchesShadow("browser", options.shadow, live);
      }
    },
    respond: async (playerId, promptOrId, response) => {
      await api.start();
      const promptId = resolvePromptInstanceId(playerId, promptOrId);

      const previousVersion =
        options.runner === "embedded"
          ? (options.embedded?.tracker.snapshot().version ?? 0)
          : options.runner === "browser"
            ? ((await options.browser?.bridge.snapshot())?.version ?? 0)
            : 0;

      if (options.runner === "embedded" && options.embedded) {
        const { data, error } = await submitInput({
          path: { sessionId: options.embedded.sessionId },
          body: {
            input: {
              kind: "promptResponse",
              playerId,
              promptId,
              response: JSON.stringify(response),
            },
            expectedVersion: previousVersion,
          },
        });
        if (error) {
          throw createSubmissionError(
            "api-error",
            undefined,
            "Failed to submit prompt response",
          );
        }
        if (data?.accepted === false) {
          const shadowError = await tryShadowPrompt(
            options.shadow,
            playerId,
            promptId,
            response,
          );
          if (!shadowError || shadowError.errorCode !== data.errorCode) {
            throw new Error(
              "Embedded prompt rejection diverged from reducer shadow state.",
            );
          }
          throw createSubmissionError(
            data.errorCode ?? undefined,
            data.message ?? undefined,
            "Prompt response rejected",
          );
        }
      } else if (options.runner === "browser" && options.browser) {
        const handled =
          (await options.browser.driver?.promptResponse?.(
            options.browser.bridge,
            {
              playerId,
              promptId,
              response,
            },
          )) === true;
        if (!handled) {
          await options.browser.bridge.submitPromptResponse(
            playerId,
            promptId,
            response,
          );
        }
      }

      await options.shadow.submitPromptResponse(playerId, promptId, response);

      if (options.runner === "embedded" && options.embedded) {
        await options.embedded.tracker.waitForVersionChange(previousVersion);
        await assertLiveMatchesShadow(
          "embedded",
          options.shadow,
          options.embedded.tracker.snapshot(),
        );
      }
      if (options.runner === "browser" && options.browser) {
        const live =
          await options.browser.bridge.waitForVersionChange(previousVersion);
        await assertLiveMatchesShadow("browser", options.shadow, live);
      }
    },
    windowAction: async (playerId, windowOrId, actionOrCommand, params) => {
      await api.start();
      const windowId = resolveWindowInstanceId(playerId, windowOrId);
      const actionType =
        typeof actionOrCommand === "string"
          ? actionOrCommand
          : actionOrCommand.type;
      const actionParams =
        typeof actionOrCommand === "string"
          ? (params ?? {})
          : (actionOrCommand.params ?? {});

      const previousVersion =
        options.runner === "embedded"
          ? (options.embedded?.tracker.snapshot().version ?? 0)
          : options.runner === "browser"
            ? ((await options.browser?.bridge.snapshot())?.version ?? 0)
            : 0;

      if (options.runner === "embedded" && options.embedded) {
        const { data, error } = await submitInput({
          path: { sessionId: options.embedded.sessionId },
          body: {
            input: {
              kind: "windowAction",
              playerId,
              windowId,
              actionType,
              params: JSON.stringify(actionParams),
            },
            expectedVersion: previousVersion,
          },
        });
        if (error) {
          throw createSubmissionError(
            "api-error",
            undefined,
            "Failed to submit window action",
          );
        }
        if (data?.accepted === false) {
          const shadowError = await tryShadowWindow(
            options.shadow,
            playerId,
            windowId,
            actionType,
            actionParams,
          );
          if (!shadowError || shadowError.errorCode !== data.errorCode) {
            throw new Error(
              "Embedded window rejection diverged from reducer shadow state.",
            );
          }
          throw createSubmissionError(
            data.errorCode ?? undefined,
            data.message ?? undefined,
            "Window action rejected",
          );
        }
      } else if (options.runner === "browser" && options.browser) {
        const handled =
          (await options.browser.driver?.windowAction?.(
            options.browser.bridge,
            {
              playerId,
              windowId,
              actionType,
              params: actionParams,
            },
          )) === true;
        if (!handled) {
          await options.browser.bridge.submitWindowAction(
            playerId,
            windowId,
            actionType,
            actionParams,
          );
        }
      }

      await options.shadow.submitWindowAction(
        playerId,
        windowId,
        actionType,
        actionParams,
      );

      if (options.runner === "embedded" && options.embedded) {
        await options.embedded.tracker.waitForVersionChange(previousVersion);
        await assertLiveMatchesShadow(
          "embedded",
          options.shadow,
          options.embedded.tracker.snapshot(),
        );
      }
      if (options.runner === "browser" && options.browser) {
        const live =
          await options.browser.bridge.waitForVersionChange(previousVersion);
        await assertLiveMatchesShadow("browser", options.shadow, live);
      }
    },
    expectPrompt: async (playerId, promptId) => {
      await api.start();
      const started = Date.now();
      while (Date.now() - started < DEFAULT_TIMEOUT_MS) {
        const prompt =
          options.shadow
            .promptsForPlayer(playerId)
            .find((candidate) => candidate.promptId === promptId) ?? null;
        if (prompt) {
          if (options.runner === "embedded" && options.embedded) {
            await assertLiveMatchesShadow(
              "embedded",
              options.shadow,
              options.embedded.tracker.snapshot(),
            );
          }
          if (options.runner === "browser" && options.browser) {
            await assertLiveMatchesShadow(
              "browser",
              options.shadow,
              await options.browser.bridge.snapshot(),
            );
          }
          return prompt;
        }
        await sleep(20);
      }
      throw new Error(`Timed out waiting for prompt '${promptId}'.`);
    },
    expectWindow: async (windowId) => {
      await api.start();
      const started = Date.now();
      while (Date.now() - started < DEFAULT_TIMEOUT_MS) {
        const window =
          options.shadow
            .windowInstances()
            .find((candidate) => candidate.windowId === windowId) ?? null;
        if (window) {
          if (options.runner === "embedded" && options.embedded) {
            await assertLiveMatchesShadow(
              "embedded",
              options.shadow,
              options.embedded.tracker.snapshot(),
            );
          }
          if (options.runner === "browser" && options.browser) {
            await assertLiveMatchesShadow(
              "browser",
              options.shadow,
              await options.browser.bridge.snapshot(),
            );
          }
          return window;
        }
        await sleep(20);
      }
      throw new Error(`Timed out waiting for window '${windowId}'.`);
    },
  };

  return {
    game: api,
    players: () => options.shadow.playerOrder(),
    state: () => options.shadow.phase(),
    view: (playerId) => options.shadow.view(playerId),
    prompts: (playerId) => options.shadow.promptsForPlayer(playerId),
    windows: (playerId) => options.shadow.windowsForPlayer(playerId),
    expect,
  };
}

async function tryShadowAction(
  shadow: ShadowReducerRuntime,
  playerId: string,
  actionType: string,
  params: Record<string, unknown>,
): Promise<SubmissionError | null> {
  try {
    await shadow.submitAction(playerId, actionType, params);
    throw new Error("Expected shadow action to reject.");
  } catch (error) {
    if (
      error instanceof Error &&
      (error as SubmissionError).name === "SubmissionError"
    ) {
      return error as SubmissionError;
    }
    throw error;
  }
}

