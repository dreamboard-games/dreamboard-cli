function shouldRefreshReducerTestingTypes(
  existingContent: string | null,
): boolean {
  if (
    existingContent === null ||
    existingContent.trim().length === 0 ||
    existingContent === TESTING_TYPES_STUB ||
    existingContent.startsWith(GENERATED_TESTING_TYPES_PREFIX)
  ) {
    return true;
  }
  return false;
}

async function discoverFiles(root: string, suffix: string): Promise<string[]> {
  if (!(await exists(root))) {
    return [];
  }
  const entries = await readdir(root, { withFileTypes: true });
  const files: string[] = [];
  for (const entry of entries) {
    const entryPath = path.join(root, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await discoverFiles(entryPath, suffix)));
      continue;
    }
    if (entry.isFile() && entry.name.endsWith(suffix)) {
      files.push(entryPath);
    }
  }
  files.sort();
  return files;
}

function isFunction(value: unknown): value is (...args: never[]) => unknown {
  return typeof value === "function";
}

function parseTypedBaseDefinition(value: unknown): TypedBaseDefinition {
  if (
    typeof value !== "object" ||
    value === null ||
    !("id" in value) ||
    typeof value.id !== "string" ||
    !("seed" in value) ||
    typeof value.seed !== "number" ||
    !("players" in value) ||
    typeof value.players !== "number" ||
    !("setup" in value) ||
    !isFunction(value.setup)
  ) {
    throw new Error("Invalid reducer-native base definition.");
  }
  const setupProfileId =
    "setupProfileId" in value && typeof value.setupProfileId === "string"
      ? value.setupProfileId
      : undefined;
  return {
    ...(value as TypedBaseDefinition),
    setupProfileId,
  };
}

function parseTypedScenarioDefinition(value: unknown): TypedScenarioDefinition {
  if (
    typeof value !== "object" ||
    value === null ||
    !("id" in value) ||
    typeof value.id !== "string" ||
    !("from" in value) ||
    typeof value.from !== "string" ||
    !("when" in value) ||
    !isFunction(value.when) ||
    !("then" in value) ||
    !isFunction(value.then)
  ) {
    throw new Error("Invalid reducer-native scenario definition.");
  }
  return value as TypedScenarioDefinition;
}

