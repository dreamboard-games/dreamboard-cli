class KotlinSeededRandom {
  private x: number;
  private y: number;
  private z: number;
  private w: number;
  private v: number;
  private addend: number;

  constructor(seed: number) {
    const seedBig = BigInt.asIntN(64, BigInt(Math.trunc(seed)));
    const seed1 = Number(BigInt.asIntN(32, seedBig));
    const seed2 = Number(BigInt.asIntN(32, seedBig >> 32n));

    this.x = seed1 | 0;
    this.y = seed2 | 0;
    this.z = 0;
    this.w = 0;
    this.v = ~seed1;
    this.addend = ((seed1 << 10) ^ (seed2 >>> 4)) | 0;

    if ((this.x | this.y | this.z | this.w | this.v) === 0) {
      throw new Error(
        "Kotlin random seed must initialize at least one state word.",
      );
    }

    for (let index = 0; index < 64; index += 1) {
      this.nextInt();
    }
  }

  nextInt(): number {
    let next = this.x | 0;
    next ^= next >>> 2;
    this.x = this.y;
    this.y = this.z;
    this.z = this.w;
    const currentV = this.v | 0;
    this.w = currentV;
    next = (next ^ (next << 1) ^ currentV ^ (currentV << 4)) | 0;
    this.v = next;
    this.addend = (this.addend + 362437) | 0;
    return (next + this.addend) | 0;
  }

  private nextBits(bitCount: number): number {
    if (bitCount <= 0) {
      return 0;
    }
    return this.nextInt() >>> (32 - bitCount);
  }

  nextIntBound(bound: number): number {
    if (bound <= 0) {
      throw new Error("bound must be positive");
    }

    if ((bound & -bound) === bound) {
      const bitCount = 31 - Math.clz32(bound);
      return this.nextBits(bitCount);
    }

    let value: number;
    while (true) {
      const bits = this.nextInt() >>> 1;
      value = bits % bound;
      if (bits - value + (bound - 1) >= 0) {
        return value;
      }
    }
  }

  nextLong(): number {
    const high = BigInt(this.nextInt());
    const low = BigInt(this.nextInt());
    return Number(BigInt.asIntN(64, (high << 32n) + low));
  }
}

function shuffleWithKotlinRandom<T>(
  values: readonly T[],
  random: KotlinSeededRandom,
): T[] {
  const next = [...values];
  for (let index = next.length - 1; index > 0; index -= 1) {
    const swapIndex = random.nextIntBound(index + 1);
    const current = next[index];
    next[index] = next[swapIndex] as T;
    next[swapIndex] = current as T;
  }
  return next;
}

