function baseStateKey(baseId: string): string {
  return baseId;
}

function validateTypedScenarioBases(
  scenarios: LoadedScenario[],
  basesById: Map<string, LoadedBase>,
): void {
  const available = new Set(basesById.keys());
  const invalid = scenarios
    .filter((scenario) => !available.has(scenario.definition.from))
    .map(
      (scenario) =>
        path.relative(process.cwd(), scenario.filePath) +
        ` -> '${scenario.definition.from}'`,
    );

  if (invalid.length > 0) {
    throw new Error(
      `Unknown scenario base(s): ${invalid.join(", ")}. Available bases: ${Array.from(available).sort().join(", ")}`,
    );
  }
}

export async function writeReducerNativeGeneratedFiles(options: {
  projectRoot: string;
  bases: LoadedBase[];
  scenarios: LoadedScenario[];
  compiledResultId?: string;
  gameId?: string;
  debug?: boolean;
}): Promise<void> {
  await ensureReducerNativeTestingFiles(options.projectRoot);

  const manifest = await loadManifest(options.projectRoot);
  const manifestHash = hashContent(JSON.stringify(manifest));
  const appBundleHash = hashContent(
    await bundleTypeScriptModuleText(
      path.join(options.projectRoot, "app", "game.ts"),
    ),
  );
  const uiContractHash = hashContent(
    await bundleTypeScriptModuleText(
      path.join(options.projectRoot, "shared", "generated", "ui-contract.ts"),
    ),
  );

  const generatedDir = path.join(options.projectRoot, "test", "generated");
  await ensureDir(generatedDir);
  const baseStates: BaseStateFile = {};
  const [gameModule, manifestContractModule] = await Promise.all([
    importTypeScriptModule<Record<string, unknown>>(
      path.join(options.projectRoot, "app", "game.ts"),
    ),
    importTypeScriptModule<Record<string, unknown>>(
      path.join(options.projectRoot, "shared", "manifest-contract.ts"),
    ),
  ]);
  const createInitialTable =
    typeof manifestContractModule.createInitialTable === "function"
      ? (manifestContractModule.createInitialTable as GeneratedInitialTableFactory)
      : null;
  for (const base of options.bases) {
    const effectiveSetup = resolveEffectiveBaseSetup({
      manifest,
      base: base.definition,
    });
    const shadow = new ShadowReducerRuntime(
      manifest,
      gameModule.default,
      createInitialTable,
      base.definition.seed,
      base.definition.players,
      effectiveSetup.setupProfileId,
      options.debug ?? false,
    );
    const context = await createScenarioContext({
      runner: "reducer",
      shadow,
    });
    await context.game.start();
    await base.definition.setup({ game: context.game });
    baseStates[baseStateKey(base.definition.id)] = {
      key: baseStateKey(base.definition.id),
      base: base.definition.id,
      snapshot: shadow.rawState(),
      fingerprint: {
        base: base.definition.id,
        seed: base.definition.seed,
        players: base.definition.players,
        baseBundleHash: base.bundleHash,
        manifestHash,
        appBundleHash,
        uiContractHash,
        setupProfileId: effectiveSetup.setupProfileId,
        compiledResultId: options.compiledResultId,
        gameId: options.gameId,
      },
    };
  }

  const header =
    "// Generated by dreamboard test generate. Do not edit by hand.\n";
  await writeTextFile(
    path.join(generatedDir, "base-states.generated.ts"),
    `${header}export const BASE_STATES = ${JSON.stringify(baseStates, null, 2)} as const;\n`,
  );
  await writeTextFile(
    path.join(generatedDir, "base-states.generated.d.ts"),
    `${header}export declare const BASE_STATES: Record<string, unknown>;\n`,
  );
  await writeTextFile(
    path.join(generatedDir, "scenario-manifest.generated.ts"),
    `${header}export const SCENARIO_MANIFEST = ${JSON.stringify(
      options.scenarios.map((scenario) => ({
        id: scenario.definition.id,
        filePath: path.relative(generatedDir, scenario.filePath),
        base: scenario.definition.from,
        runners: normalizeScenarioRunners(scenario.definition.runners),
      })),
      null,
      2,
    )} as const;\n`,
  );
  await writeJsonFile(path.join(generatedDir, ".generation-meta.json"), {
    generatedAt: new Date().toISOString(),
    scenarioCount: options.scenarios.length,
    baseStateCount: options.bases.length,
  });
}

async function loadGeneratedBaseStates(
  projectRoot: string,
): Promise<BaseStateFile | null> {
  const filePath = path.join(
    projectRoot,
    "test",
    "generated",
    "base-states.generated.ts",
  );
  if (!(await exists(filePath))) {
    return null;
  }
  const module =
    await importTypeScriptModule<Record<string, unknown>>(filePath);
  return (module.BASE_STATES as BaseStateFile | undefined) ?? null;
}

function validateGeneratedFingerprint(options: {
  generated: BaseStateArtifact["fingerprint"];
  current: BaseStateArtifact["fingerprint"];
}): void {
  const mismatches: string[] = [];
  if (options.generated.baseBundleHash !== options.current.baseBundleHash) {
    mismatches.push("base module changed");
  }
  if (options.generated.manifestHash !== options.current.manifestHash) {
    mismatches.push("manifest changed");
  }
  if (options.generated.appBundleHash !== options.current.appBundleHash) {
    mismatches.push("game reducer bundle changed");
  }
  if (options.generated.uiContractHash !== options.current.uiContractHash) {
    mismatches.push("ui contract changed");
  }
  if (
    options.generated.compiledResultId &&
    options.current.compiledResultId &&
    options.generated.compiledResultId !== options.current.compiledResultId
  ) {
    mismatches.push("compiled result changed");
  }
  if (
    options.generated.gameId &&
    options.current.gameId &&
    options.generated.gameId !== options.current.gameId
  ) {
    mismatches.push("game id changed");
  }
  if (
    options.generated.seed !== options.current.seed ||
    options.generated.players !== options.current.players
  ) {
    mismatches.push("base seed/player count changed");
  }
  if (
    (options.generated.setupProfileId ?? null) !==
    (options.current.setupProfileId ?? null)
  ) {
    mismatches.push("base setup profile changed");
  }
  if (mismatches.length > 0) {
    throw new Error(
      `${mismatches.join("; ")}. Run 'dreamboard test generate' to refresh reducer-native base artifacts.`,
    );
  }
}

async function currentFingerprint(options: {
  projectRoot: string;
  base: LoadedBase;
  compiledResultId?: string;
  gameId?: string;
}): Promise<BaseStateArtifact["fingerprint"]> {
  const manifest = await loadManifest(options.projectRoot);
  const effectiveSetup = resolveEffectiveBaseSetup({
    manifest,
    base: options.base.definition,
  });
  return {
    base: options.base.definition.id,
    seed: options.base.definition.seed,
    players: options.base.definition.players,
    baseBundleHash: options.base.bundleHash,
    manifestHash: hashContent(JSON.stringify(manifest)),
    appBundleHash: hashContent(
      await bundleTypeScriptModuleText(
        path.join(options.projectRoot, "app", "game.ts"),
      ),
    ),
    uiContractHash: hashContent(
      await bundleTypeScriptModuleText(
        path.join(options.projectRoot, "shared", "generated", "ui-contract.ts"),
      ),
    ),
    setupProfileId: effectiveSetup.setupProfileId,
    compiledResultId: options.compiledResultId,
    gameId: options.gameId,
  };
}

export async function generateReducerNativeArtifacts(options: {
  projectRoot: string;
  compiledResultId?: string;
  gameId?: string;
  scenarioPath?: string;
  debug?: boolean;
}): Promise<{
  bases: LoadedBase[];
  scenarios: LoadedScenario[];
}> {
  await ensureReducerNativeTestingFiles(options.projectRoot);
  const [bases, scenarios] = await Promise.all([
    loadTypedBases(options.projectRoot),
    loadTypedScenarios(options.projectRoot, {
      scenarioPath: options.scenarioPath,
    }),
  ]);
  const basesById = new Map(bases.map((base) => [base.definition.id, base]));
  validateTypedScenarioBases(scenarios, basesById);
  await writeReducerNativeGeneratedFiles({
    projectRoot: options.projectRoot,
    bases,
    scenarios,
    compiledResultId: options.compiledResultId,
    gameId: options.gameId,
    debug: options.debug,
  });
  return { bases, scenarios };
}

export async function runReducerNativeScenarios(options: {
  projectRoot: string;
  projectConfig: ProjectConfig;
  resolvedConfig: ResolvedConfig;
  runner: TestRunnerName;
  scenarioPath?: string;
  compiledResultId?: string;
  gameId?: string;
  webBaseUrl?: string;
  debug?: boolean;
}): Promise<ReducerNativeScenarioSummary> {
  await ensureReducerNativeTestingFiles(options.projectRoot);

  const [
    bases,
    scenarios,
    generatedBaseStates,
    manifest,
    gameModule,
    manifestContractModule,
  ] = await Promise.all([
    loadTypedBases(options.projectRoot),
    loadTypedScenarios(options.projectRoot, {
      scenarioPath: options.scenarioPath,
    }),
    loadGeneratedBaseStates(options.projectRoot),
    loadManifest(options.projectRoot),
    importTypeScriptModule<Record<string, unknown>>(
      path.join(options.projectRoot, "app", "game.ts"),
    ),
    importTypeScriptModule<Record<string, unknown>>(
      path.join(options.projectRoot, "shared", "manifest-contract.ts"),
    ),
  ]);
  const createInitialTable =
    typeof manifestContractModule.createInitialTable === "function"
      ? (manifestContractModule.createInitialTable as GeneratedInitialTableFactory)
      : null;

  const basesById = new Map(bases.map((base) => [base.definition.id, base]));
  validateTypedScenarioBases(scenarios, basesById);

  const runnerScenarios = scenarios.filter((scenario) =>
    normalizeScenarioRunners(scenario.definition.runners).includes(
      options.runner,
    ),
  );

  let embeddedHarness: EmbeddedHarnessSession | null = null;
  let browser: Browser | null = null;
  let page: Page | null = null;
  let browserBridge: BrowserBridgeClient | null = null;
  let browserDriver: BrowserRunnerDriver | null = null;

  if (options.runner === "embedded") {
    embeddedHarness = await startEmbeddedHarnessSession({
      projectRoot: options.projectRoot,
      projectConfig: options.projectConfig,
      configureClient: true,
    });
  }

  if (options.runner === "browser") {
    const webBaseUrl = options.webBaseUrl ?? options.projectConfig.webBaseUrl;
    if (!webBaseUrl) {
      throw new Error(
        "Browser runner requires a local webBaseUrl. Start the local web stack and configure the project for env local.",
      );
    }
    browserDriver = await loadBrowserDriver(options.projectRoot);
    const opened = await openBrowserPage(
      `${webBaseUrl}/_dev`,
      options.resolvedConfig,
    );
    browser = opened.browser;
    page = opened.page;
  }

  try {
    let passed = 0;
    let failed = 0;
    const results: ReducerNativeScenarioResult[] = [];

    for (const scenario of runnerScenarios) {
      const base = basesById.get(scenario.definition.from);
      if (!base) {
        throw new Error(
          `Missing typed base '${scenario.definition.from}' for scenario '${scenario.definition.id}'.`,
        );
      }

      if (generatedBaseStates?.[baseStateKey(base.definition.id)]) {
        validateGeneratedFingerprint({
          generated:
            generatedBaseStates[baseStateKey(base.definition.id)]!.fingerprint,
          current: await currentFingerprint({
            projectRoot: options.projectRoot,
            base,
            compiledResultId: options.compiledResultId,
            gameId: options.gameId,
          }),
        });
      } else if (options.runner !== "reducer") {
        throw new Error(
          "Missing reducer-native generated base artifacts. Run 'dreamboard test generate' first.",
        );
      }

      try {
        const effectiveSetup = resolveEffectiveBaseSetup({
          manifest,
          base: base.definition,
        });
        const shadow = new ShadowReducerRuntime(
          manifest,
          gameModule.default,
          createInitialTable,
          base.definition.seed,
          base.definition.players,
          effectiveSetup.setupProfileId,
          options.debug ?? false,
        );

        let embedded:
          | {
              sessionId: string;
              tracker: EmbeddedGameplayTracker;
            }
          | undefined;
        try {
          if (options.runner === "embedded" && embeddedHarness) {
            const { data: session, error: sessionError } = await createSession({
              path: { gameId: embeddedHarness.gameId },
              body: {
                compiledResultId: embeddedHarness.compiledResultId,
                seed: base.definition.seed,
                playerCount: base.definition.players,
                autoAssignSeats: true,
                setupProfileId: effectiveSetup.setupProfileId ?? undefined,
              },
            });
            if (!session || sessionError) {
              throw new Error("Failed to create embedded harness session.");
            }
            const tracker = new EmbeddedGameplayTracker(session.sessionId);
            const { error: startError } = await startGame({
              path: { sessionId: session.sessionId },
            });
            if (startError) {
              throw new Error("Failed to start embedded harness session.");
            }
            await tracker.connect();
            await tracker.waitForBootstrap();
            embedded = {
              sessionId: session.sessionId,
              tracker,
            };
          }

          if (options.runner === "browser" && page) {
            const gameId = options.gameId ?? options.projectConfig.gameId;
            const compiledResultId =
              options.compiledResultId ??
              options.projectConfig.compile?.latestSuccessful?.resultId;
            if (!compiledResultId) {
              throw new Error(
                "Browser runner requires a compiled result. Compile the workspace first.",
              );
            }
            const { data: session, error: sessionError } = await createSession({
              path: { gameId },
              body: {
                compiledResultId,
                seed: base.definition.seed,
                playerCount: base.definition.players,
                autoAssignSeats: true,
                setupProfileId: effectiveSetup.setupProfileId ?? undefined,
              },
            });
            if (!session || sessionError) {
              throw new Error("Failed to create browser-runner session.");
            }
            const { error: startError, data: started } = await startGame({
              path: { sessionId: session.sessionId },
            });
            if (startError || !started) {
              throw new Error("Failed to start browser-runner session.");
            }
            await page.goto(
              `${options.webBaseUrl ?? options.projectConfig.webBaseUrl}/_dev/play/${started.shortCode}`,
              { waitUntil: "domcontentloaded" },
            );
            await waitForGameReady(page);
            browserBridge = await createBrowserBridgeClient(page);
            await browserDriver?.onReady?.(browserBridge);
          }

          const context = await createScenarioContext({
            runner: options.runner,
            shadow,
            embedded,
            browser:
              options.runner === "browser" && browserBridge
                ? {
                    bridge: browserBridge,
                    driver: browserDriver,
                  }
                : undefined,
          });

          await context.game.start();
          await base.definition.setup({ game: context.game });
          shadow.clearHistory();
          await scenario.definition.when(context);
          await scenario.definition.then(context);
          passed += 1;
          results.push({
            id: scenario.definition.id,
            success: true,
          });
        } finally {
          embedded?.tracker.close();
        }
      } catch (error) {
        failed += 1;
        results.push({
          id: scenario.definition.id,
          success: false,
          error:
            error instanceof Error
              ? error.message
              : `Scenario '${scenario.definition.id}' failed.`,
        });
      }
    }

    return { passed, failed, results };
  } finally {
    if (embeddedHarness) {
      await embeddedHarness.stop();
    }
    if (browser) {
      await browser.close();
    }
  }
}
