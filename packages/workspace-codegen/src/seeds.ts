type GameDefinition = typeof game;

type RequiredKeys<T extends object> = {
  [Key in keyof T]-?: {} extends Pick<T, Key> ? never : Key;
}[keyof T];

type InvocationArgs<Params> = [Params] extends [undefined]
  ? []
  : Params extends object
    ? keyof Params extends never
      ? []
      : RequiredKeys<Params> extends never
        ? [] | [Params]
        : [Params]
    : [Params];

type CommandShape<Name extends string, Params> = [Params] extends [undefined]
  ? { type: Name }
  : Params extends object
    ? keyof Params extends never
      ? { type: Name }
      : RequiredKeys<Params> extends never
        ? { type: Name; params?: Params }
        : { type: Name; params: Params }
    : { type: Name; params: Params };

type CommandCreator<Name extends string, Params> = (
  ...args: InvocationArgs<Params>
) => CommandShape<Name, Params>;

function createCommand<Name extends string>(type: Name) {
  return ((...args: [] | [unknown]) => {
    const params = args[0];
    return typeof params === "undefined" ? { type } : { type, params };
  }) as CommandCreator<Name, unknown>;
}

function createCommandNamespace() {
  return new Proxy(Object.create(null), {
    get(_target, prop) {
      if (typeof prop !== "string") {
        return undefined;
      }
      return createCommand(prop);
    },
    has(_target, prop) {
      return typeof prop === "string";
    },
  });
}

