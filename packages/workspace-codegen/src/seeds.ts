function resolveUiSdkComponentsRoot(): string {
  try {
    const uiSdkPackageJsonPath = require.resolve(
      "@dreamboard/ui-sdk/package.json",
    );
    const installedComponentsRoot = path.join(
      path.dirname(uiSdkPackageJsonPath),
      "src",
      "components",
    );
    if (existsSync(installedComponentsRoot)) {
      return installedComponentsRoot;
    }
  } catch {
    // Fall through to the repo-local source checkout lookup below.
  }

  let currentDir = MODULE_DIR;
  while (true) {
    const repoLocalComponentsRoot = path.join(
      currentDir,
      "packages",
      "ui-sdk",
      "src",
      "components",
    );
    if (
      existsSync(path.join(currentDir, "pnpm-workspace.yaml")) &&
      existsSync(repoLocalComponentsRoot)
    ) {
      return repoLocalComponentsRoot;
    }

    const parentDir = path.dirname(currentDir);
    if (parentDir === currentDir) {
      break;
    }
    currentDir = parentDir;
  }

  throw new Error(
    "Could not resolve Dreamboard UI SDK component seeds from an installed package or source checkout.",
  );
}

function walkUiSdkComponentFiles(rootDir: string): string[] {
  const componentFiles: string[] = [];
  const stack = [rootDir];

  while (stack.length > 0) {
    const currentDir = stack.pop();
    if (!currentDir) continue;

    for (const entry of readdirSync(currentDir, { withFileTypes: true })) {
      if (entry.name === "__fixtures__") {
        continue;
      }

      const entryPath = path.join(currentDir, entry.name);
      if (entry.isDirectory()) {
        stack.push(entryPath);
        continue;
      }

      if (!entry.isFile()) {
        continue;
      }

      componentFiles.push(
        path.relative(rootDir, entryPath).replaceAll("\\", "/"),
      );
    }
  }

  componentFiles.sort((left, right) => left.localeCompare(right));
  return componentFiles;
}

function rewriteUiSdkComponentImports(content: string): string {
  let rewritten = content;

  for (const [from, to] of UI_SDK_COMPONENT_IMPORT_REWRITES) {
    rewritten = rewritten.replaceAll(`"${from}"`, `"${to}"`);
    rewritten = rewritten.replaceAll(`'${from}'`, `'${to}'`);
  }

  return rewritten;
}

function generateUiSdkComponentSeeds(): Record<string, string> {
  const sourceRoot = resolveUiSdkComponentsRoot();

  return Object.fromEntries(
    walkUiSdkComponentFiles(sourceRoot).map((relativePath) => {
      const sourcePath = path.join(sourceRoot, relativePath);
      const content = readFileSync(sourcePath, "utf8");

      return [
        `${UI_SDK_COMPONENT_SEED_PREFIX}${relativePath}`,
        rewriteUiSdkComponentImports(content),
      ];
    }),
  );
}

